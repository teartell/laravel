                <h1 class="display-4 fst-italic">Valve представила портативный игровой ПК Steam Deck</h1>
                <p class="lead my-3">В странах «первой волны» он поступит в продажу в декабре 2021-го.</p>
                <p>Страница устройства с подробностями о нём появилась в Steam. Пока что можно лишь зарезервировать Deck для покупки, чтобы получить уведомление, когда оно поступит в продажу.

                    Информация о том, что Valve работает над подобным устройством, появилась в сети за полтора месяца до официального релиза. Сначала упоминания о портативном ПК нашли в базе данных Steam, а позже информацию подтвердили источники СМИ.

                    Общие подробности об устройстве
                    Стоимость портативного компьютера составит от 399 долларов (около 30 тысяч рублей). Региональные цены на данный момент неизвестны.
                    В Valve обещают, что на Deck будут доступны все игры из библиотеки Steam — достаточно лишь войти в аккаунт.
                    С устройства также можно будет пользоваться остальными функциями, привязанными к учётной записи — например, списком друзей, чатами, магазином и облачными сохранениями.
                    Deck работает на специально адаптированной версии SteamOS 3.0 и включает слой совместимости Proton — за счёт него «играм, скорее всего, не потребуется портирование».
                    На компьютер позволят устанавливать Windows и стороннее ПО.
                    В системе специально переработали оверлей и различные меню для удобства управления на небольшом экране.
                    Технические характеристики
                    В продажу поступит три модели, идентичные во всём, кроме объёма накопителя: 64 ГБ (eMMC), 256 или 512 ГБ (NVMe SSD). Кроме того, в каждой будет слот для карты microSD.
                    Устройство оснащено гибридным процессором AMD, в котором используются микроархитектуры Zen 2 и RDNA 2. Центральный процессор работает на частоте 2,4-3,5 Ггц, а графический — на частоте 1,0-1,6 Ггц.
                    16 ГБ оперативной памяти LPDDR5.
                    В Deck будет семидюймовый сенсорный дисплей с разрешением 1280x800, яркостью 400 нт, а также частотой обновления 60 Гц.
                    Компьютер оснащён портом USB-C и аудиоразъёмом на 3,5 мм, также поддерживает Bluetooth 5.0 и Wi-Fi подключение.
                    Приблизительный вес устройства — 669 грамм, а размеры — 298 на 117 на 49 мм.
                    Отдельно в продажу поступит док-станция с портами для периферийных устройств и подключения к ТВ, а также интернету по кабелю. Подробностями о ней обещают поделиться позже.
